CLIENT_DETECTORS = {
    "codex":       lambda: (Path.home() / ".codex" / "config.toml").exists() or (Path.home() / ".codex").exists(),
    "claude-code": lambda: bool(_which("claude")),
    "cursor":      lambda: (Path.home() / ".cursor").exists() or (Path.home() / "Library" / "Application Support" / "Cursor").exists(),
    "windsurf":    lambda: (Path.home() / ".codeium" / "windsurf").exists() or (Path.home() / "Library" / "Application Support" / "Windsurf").exists(),
}

CLIENT_REGISTRARS = {
    "codex":       register_codex,
    "claude-code": register_claude_code,
    "cursor":      register_cursor,
    "windsurf":    register_windsurf,
}


# -------------- BGE prefetch (optional) --------------

def prefetch_bge_model(model_name: str = "BAAI/bge-small-en-v1.5") -> None:
    """Download the BGE weights into HuggingFace's cache so the first
    Memory(...) call is fast."""
    try:
        from sentence_transformers import SentenceTransformer  # type: ignore
    except ImportError:
        print("  ⚠ sentence-transformers not installed; skipping model pre-fetch.")
        return
    print(f"  Downloading {model_name} (one-time, ~33MB)...")
    SentenceTransformer(model_name)
    print("  ✓ BGE model cached.")


# -------------- main --------------

def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(
        prog="recall-setup",
        description="Recall — interactive post-install setup.",
    )
    p.add_argument("--yes", "-y", action="store_true", help="Skip prompts; use defaults.")
    p.add_argument("--client", default=None,
                   help="codex | claude-code | cursor | windsurf | all | none")
    p.add_argument("--openai-key", dest="openai_key", default=None)
    p.add_argument("--openai-base-url", dest="openai_base_url", default=None)
    p.add_argument("--openai-model", dest="openai_model", default=None)
    p.add_argument("--db-dir", dest="db_dir", default=None)
    p.add_argument("--no-prefetch", action="store_true",
                   help="Skip downloading the BGE model now.")
    args = p.parse_args(argv)

    print("\nRecall — post-install setup\n")

    detected = {k: fn() for k, fn in CLIENT_DETECTORS.items()}
    if args.yes or args.client is not None:
        # non-interactive
        c = (args.client or "none").lower()
        chosen = (
            list(CLIENT_REGISTRARS.keys()) if c == "all"
            else [] if c == "none"
            else [s.strip() for s in c.split(",") if s.strip()]
        )
        openai_key = args.openai_key or ""
        openai_base_url = args.openai_base_url or ""
        openai_model = args.openai_model or ""
        db_dir = args.db_dir or str(Path.home() / ".recall")
    else:
        # interactive
        print("Detected MCP clients on this machine:")
        for k, v in detected.items():
            print(f"  {'✓' if v else '·'} {k}")
        print()

        choice = _pick(
            "Which MCP client(s) to register with?",
            [
                ("codex",       "Codex CLI",        "writes ~/.codex/config.toml"),
                ("claude-code", "Claude Code",      "claude mcp add recall (user scope)"),
                ("cursor",      "Cursor",           "writes ~/.cursor/mcp.json"),
                ("windsurf",    "Windsurf",         "writes ~/.codeium/windsurf/mcp_config.json"),
                ("all",         "All of the above", ""),
                ("none",        "Skip — just save config", ""),
            ],
            "all",
        )
        chosen = (
            list(CLIENT_REGISTRARS.keys()) if choice == "all"
            else [] if choice == "none"
            else [choice]
        )
        print()

        want_llm = _yesno("Configure an OpenAI / TokenRouter API key for real bounded_answer?", default=False)
        if want_llm:
            openai_key      = _ask("  API key (input echoed — paste from a keychain if needed)")
            openai_base_url = _ask("  Base URL", default="https://api.openai.com/v1")
            openai_model    = _ask("  Model", default="gpt-4o-mini")
        else:
            openai_key = ""
            openai_base_url = ""
            openai_model = ""
        print()

        db_dir = _ask("Default DB directory", default=str(Path.home() / ".recall"))
        print()

    # ------ write config ------
    values = {
        "OPENAI_API_KEY":     openai_key,
        "OPENAI_BASE_URL":    openai_base_url,
        "RECALL_OPENAI_MODEL": openai_model,
        "RECALL_DB_DIR":      db_dir,
    }
    env_path = write_env_file(values)
    print(f"✓ Wrote config to {env_path}\n")

    # ------ register MCP clients ------
    if chosen:
        print("Registering MCP server with chosen client(s):")
        for c in chosen:
            fn = CLIENT_REGISTRARS.get(c)
            if fn:
                fn(values)
        print()

    # ------ optional model prefetch ------
    if not args.no_prefetch:
        try:
            print("Pre-fetching BGE model so first Memory(...) call is fast:")
            prefetch_bge_model()
            print()
        except Exception as e:
            print(f"  ⚠ pre-fetch skipped: {e}\n")

    # ------ summary ------
    print("✅ Setup complete.\n")
    if chosen:
        print("Restart your MCP client(s) to load `recall`.\n")
    print("Try:")
    print("  recall me add 'first memory'")
    print("  recall me ask 'what was my first memory?'")
    print("  recall me health\n")
    print("Docs: https://github.com/yash194/recall\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
