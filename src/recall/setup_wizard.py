"""Interactive setup wizard for Recall (post-install).

Run after ``pip install typed-recall``:

    recall-setup           # full interactive flow
    recall-setup --yes     # use defaults, no prompts
    recall-setup --client claude-code --openai-key sk-...

The wizard collects:
  - OpenAI / TokenRouter API key (for real bounded_generate)
  - Base URL + model
  - Default DB directory
  - Optional MCP-client registration (Codex / Claude Code / Cursor / Windsurf)

Writes ``~/.recall/.env`` so the library, CLI, and MCP server pick up the
config automatically (auto-loaded on import via ``python-dotenv``).

Also pre-warms the BGE embedder cache on first run so subsequent
``Memory(...)`` instantiation is fast.
"""
from __future__ import annotations

import argparse
import os
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Optional


# -------------- helpers --------------

def _which(cmd: str) -> Optional[str]:
    return shutil.which(cmd)


def _ask(label: str, default: str = "") -> str:
    suffix = f" [{default}]" if default else ""
    try:
        ans = input(f"{label}{suffix}: ").strip()
    except (EOFError, KeyboardInterrupt):
        print()
        return default
    return ans or default


def _yesno(label: str, default: bool = False) -> bool:
    suffix = "Y/n" if default else "y/N"
    try:
        ans = input(f"{label} [{suffix}]: ").strip().lower()
    except (EOFError, KeyboardInterrupt):
        print()
        return default
    if not ans:
        return default
    return ans.startswith("y")


def _pick(label: str, choices: list[tuple[str, str, str]], default: str) -> str:
    """Numbered menu. choices = [(value, label, detail), ...]."""
    print(label)
    for i, (_, lab, det) in enumerate(choices, 1):
        print(f"  {i}. {lab}" + (f"  — {det}" if det else ""))
    try:
        ans = input(f"Choice [{default}]: ").strip()
    except (EOFError, KeyboardInterrupt):
        print()
        return default
    if not ans:
        return default
    if ans.isdigit():
        idx = int(ans) - 1
        if 0 <= idx < len(choices):
            return choices[idx][0]
    for v, _, _ in choices:
        if v.lower() == ans.lower():
            return v
    return default


# -------------- env file writer --------------

def _config_dir() -> Path:
    p = Path.home() / ".recall"
    p.mkdir(parents=True, exist_ok=True)
    return p


def write_env_file(values: dict[str, str]) -> Path:
    """Write a dotenv-format file. Quote values that need it."""
    env_path = _config_dir() / ".env"
    # Preserve any existing keys we don't override
    existing: dict[str, str] = {}
    if env_path.exists():
        for line in env_path.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            k, v = line.split("=", 1)
            existing[k.strip()] = v.strip()
    existing.update({k: v for k, v in values.items() if v})
    lines = ["# Recall config — generated by `recall-setup`. Auto-loaded on import.\n"]
    for k, v in sorted(existing.items()):
        # Quote if contains spaces or special chars
        v_str = str(v)
        if any(c in v_str for c in [" ", "#", '"', "'", "$"]):
            v_str = '"' + v_str.replace('"', '\\"') + '"'
        lines.append(f"{k}={v_str}\n")
    env_path.write_text("".join(lines))
    env_path.chmod(0o600)  # restrict, since this may contain API keys
    return env_path


# -------------- MCP client registration --------------

def _make_env_block(values: dict[str, str]) -> dict[str, str]:
    """Subset of values that should be passed via the MCP client's env block."""
    keys = ("OPENAI_API_KEY", "OPENAI_BASE_URL", "RECALL_OPENAI_MODEL", "RECALL_DB_DIR")
    return {k: v for k, v in values.items() if v and k in keys}


def register_codex(values: dict[str, str]) -> bool:
    cfg_path = Path.home() / ".codex" / "config.toml"
    cfg_path.parent.mkdir(parents=True, exist_ok=True)
    content = cfg_path.read_text() if cfg_path.exists() else ""
    # Strip prior recall block (best-effort)
    import re
    content = re.sub(
        r"\n*# ─── Recall.*?(?=\n\[(?!mcp_servers\.recall)|\Z)",
        "",
        content,
        flags=re.DOTALL,
    )
    content = re.sub(
        r"\n*\[mcp_servers\.recall\][\s\S]*?(?=\n\[(?!mcp_servers\.recall\.)|\Z)",
        "",
        content,
        flags=re.DOTALL,
    )
    content = re.sub(
        r"\n*\[mcp_servers\.recall\.env\][\s\S]*?(?=\n\[(?!mcp_servers\.recall\.)|\Z)",
        "",
        content,
        flags=re.DOTALL,
    )
    block = "\n# ─── Recall — typed-edge memory MCP server ───\n"
    block += "[mcp_servers.recall]\n"
    block += 'command = "recall-mcp"\n'
    block += 'args = []\n'
    env = _make_env_block(values)
    if env:
        block += "[mcp_servers.recall.env]\n"
        for k, v in env.items():
            block += f'{k} = "{v}"\n'
    cfg_path.write_text(content.rstrip() + "\n" + block)
    print(f"  ✓ Wrote MCP entry to {cfg_path}")
    return True


def register_claude_code(values: dict[str, str]) -> bool:
    if not _which("claude"):
        print("  ⚠ `claude` CLI not on PATH; skipping. Run manually:")
        print("       claude mcp add recall -- recall-mcp")
        return False
    subprocess.run(["claude", "mcp", "remove", "recall", "--scope", "user"],
                   capture_output=True, check=False)
    args = ["claude", "mcp", "add", "recall", "--scope", "user"]
    for k, v in _make_env_block(values).items():
        args.extend(["-e", f"{k}={v}"])
    args.extend(["--", "recall-mcp"])
    r = subprocess.run(args, check=False)
    if r.returncode == 0:
        print("  ✓ Registered with Claude Code (user scope)")
        return True
    print("  ⚠ `claude mcp add` failed; you can run manually:")
    print("       claude mcp add recall -- recall-mcp")
    return False


def register_cursor(values: dict[str, str]) -> bool:
    import json
    p = Path.home() / ".cursor" / "mcp.json"
    p.parent.mkdir(parents=True, exist_ok=True)
    cfg = {"mcpServers": {}}
    if p.exists():
        try:
            cfg = json.loads(p.read_text())
        except Exception:
            cfg = {"mcpServers": {}}
        cfg.setdefault("mcpServers", {})
    env = _make_env_block(values)
    cfg["mcpServers"]["recall"] = {"command": "recall-mcp", "args": []}
    if env:
        cfg["mcpServers"]["recall"]["env"] = env
    p.write_text(json.dumps(cfg, indent=2))
    print(f"  ✓ Wrote MCP entry to {p}")
    return True


def register_windsurf(values: dict[str, str]) -> bool:
    import json
    p = Path.home() / ".codeium" / "windsurf" / "mcp_config.json"
    p.parent.mkdir(parents=True, exist_ok=True)
    cfg = {"mcpServers": {}}
    if p.exists():
        try:
            cfg = json.loads(p.read_text())
        except Exception:
            cfg = {"mcpServers": {}}
        cfg.setdefault("mcpServers", {})
    env = _make_env_block(values)
    cfg["mcpServers"]["recall"] = {"command": "recall-mcp", "args": []}
    if env:
        cfg["mcpServers"]["recall"]["env"] = env
